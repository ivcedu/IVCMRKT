var userRole = (function() {
    return {
        isActiveMRKTStaff: function () {
            var result = new Array();
            result = db_getAdminByEmail(sessionStorage.getItem('ss_mrkt_loginEmail'));

            if (result.length === 1 && result[0]['Active'] === "1") {
                this.AdminID = result[0]['AdminID'];
                this.AdminPrivilegeID = result[0]['AdminPrivilegeID'];
            }
        },
        getActiveAdminPrivilege: function (admin_privilege_id) {
            if (admin_privilege_id === "") {
                return "";
            }

            var privilege = new Array();
            privilege = db_getAdminPrivilegeByID(admin_privilege_id);
            return privilege[0]['AccessLevel'];
        }
    };
})(userRole);
